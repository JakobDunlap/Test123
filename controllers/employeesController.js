const fs = require('fs');
const path = require('path');
const Employee = require ('../model/Employee');


//Returns employees from mongodb as url.com/employees response
const getAllEmployees = async (req, res) => {
    const jsonEmployeesPath = path.join(__dirname, '../model/employees.json');
    const jsonEmployees = JSON.parse(fs.readFileSync(jsonEmployeesPath, 'utf-8'));
    const dbEmployees = await Employee.find();
    const allEmployees = [...jsonEmployees, ...dbEmployees];
    res.json(allEmployees);
}

